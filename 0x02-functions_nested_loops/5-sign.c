#include "main.h"

/**
 * print_sign - Determines if the input greater,equal or less than zero.
 * @n the number to be checked
 */
int print_sign(int n)
{
	if (n > 0)
	{
		putchar('+');
		return (1);
	}
	else if (n == 0)
	{
		putchar('0');
		return (0);
	}
	else
	{
		putchar('-');
		return (-1);
	}
}
